package com.xunyun.infanteduplatform.domain.interaction;


public class DiscussionModel {
	// 交流id
	private Integer discussionId;
	// 交流内容
	private String discussionContent;
	// 创建时间
	private String creationTime;
	// 创建人
	private String createdBy;
	// 回复总条数
	private Integer replyCount;
	// 点赞总条数
	private Integer dynamicCount;
	// 分享总条数
	private Integer shareCount;
	// 收藏总数
	private Integer collectCount;
	// 作者头像
	private byte[] authorPhotoUrl;
	
	public Integer getCollectCount() {
		return collectCount;
	}

	public void setCollectCount(Integer collectCount) {
		this.collectCount = collectCount;
	}

	public Integer getShareCount() {
		return shareCount;
	}

	public void setShareCount(Integer shareCount) {
		this.shareCount = shareCount;
	}

	public Integer getDiscussionId() {
		return discussionId;
	}

	public void setDiscussionId(Integer discussionId) {
		this.discussionId = discussionId;
	}


	public String getDiscussionContent() {
		return discussionContent;
	}

	public void setDiscussionContent(String discussionContent) {
		this.discussionContent = discussionContent;
	}

	public String getCreationTime() {
		return creationTime;
	}

	public void setCreationTime(String nowDate) {
		this.creationTime = nowDate;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public Integer getReplyCount() {
		return replyCount;
	}

	public void setReplyCount(Integer replyCount) {
		this.replyCount = replyCount;
	}

	public Integer getDynamicCount() {
		return dynamicCount;
	}

	public void setDynamicCount(Integer dynamicCount) {
		this.dynamicCount = dynamicCount;
	}

	public byte[] getAuthorPhotoUrl() {
		return authorPhotoUrl;
	}

	public void setAuthorPhotoUrl(byte[] authorPhotoUrl) {
		this.authorPhotoUrl = authorPhotoUrl;
	}

}

package com.xunyun.infanteduplatform.domain.interaction;

import java.util.Date;

public class Dynamic {
	//点赞Id
	private Integer praiseId;
	//模块Id
	private Integer moduleId;
	//用户Id
	private Integer userId;
	//点赞时间
	private Date praiseTime;
	//类型
	private Integer dynamicType;
	//点赞用户头像
	private  String dynamicPhotoUrl;
	
	
	public String getDynamicPhotoUrl() {
		return dynamicPhotoUrl;
	}
	public void setDynamicPhotoUrl(String dynamicPhotoUrl) {
		this.dynamicPhotoUrl = dynamicPhotoUrl;
	}
	public Integer getPraiseId() {
		return praiseId;
	}
	public void setPraiseId(Integer praiseId) {
		this.praiseId = praiseId;
	}
	public Integer getModuleId() {
		return moduleId;
	}
	public void setModuleId(Integer moduleId) {
		this.moduleId = moduleId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Date getPraiseTime() {
		return praiseTime;
	}
	public void setPraiseTime(Date praiseTime) {
		this.praiseTime = praiseTime;
	}
	public Integer getDynamicType() {
		return dynamicType;
	}
	public void setDynamicType(Integer dynamicType) {
		this.dynamicType = dynamicType;
	}
	
}

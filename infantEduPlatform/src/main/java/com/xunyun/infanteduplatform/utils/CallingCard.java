package com.xunyun.infanteduplatform.utils;

import java.util.Date;

import com.xunyun.infanteduplatform.domain.SysUserLogin;

public class CallingCard {
  /**
   * 变量类型：long 变量：serialVersionUID
   */
//  private static final long serialVersionUID = 1L;
//  private Logger logger = Logger.getLogger(CallingCard.class);

  private String name;
  private String sessionId; // 登陆用户会话标识
  private Date dateCreated = new Date(); // 登陆时间
  private String loginIp; // 登陆IP
  private SysUserLogin user; // 登陆用户

  public CallingCard() {

  }

  public String getName() {
    return name;
  }


  /**
   * name的setter方法
   * 
   * @param name the name to set
   */
  public void setName(String name) {
    this.name = name;
  }


  /**
   * sessionId的getter方法
   * 
   * @return the sessionId
   */
  public String getSessionId() {
    return sessionId;
  }

  /**
   * sessionId的setter方法
   * 
   * @param sessionId the sessionId to set
   */
  public void setSessionId(String sessionId) {
    this.sessionId = sessionId;
  }

  /**
   * dateCreated的getter方法
   * 
   * @return the dateCreated
   */
  public Date getDateCreated() {
    return dateCreated;
  }

  /**
   * dateCreated的setter方法
   * 
   * @param dateCreated the dateCreated to set
   */
  public void setDateCreated(Date dateCreated) {
    this.dateCreated = dateCreated;
  }

  /**
   * loginIp的getter方法
   * 
   * @return the loginIp
   */
  public String getLoginIp() {
    return loginIp;
  }

  /**
   * loginIp的setter方法
   * 
   * @param loginIp the loginIp to set
   */
  public void setLoginIp(String loginIp) {
    this.loginIp = loginIp;
  }



  /**
   * user的getter方法
   * 
   * @return the user
   */
  public SysUserLogin getUser() {
    return user;
  }


  /**
   * user的setter方法
   * 
   * @param user the user to set
   */
  public void setUser(SysUserLogin user) {
    this.user = user;
  }

}
